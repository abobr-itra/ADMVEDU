    case movie = "Movie"
    case podcast = "Podcast"
    case music = "Music"
    case musicVideo = "Music Video"
    case audiobook = "Audiobook"
    case shortFilm = "Short Film"
    case tvShow = "TV Show"
    case software = "Software"
    case ebook = "Ebook"
    case all = "All"

}
